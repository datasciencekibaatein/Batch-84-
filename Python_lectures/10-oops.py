#What is oops?
'''
Object-oriented programming is a way of writing programs by thinking in terms of real-world things.

Instead of only writing steps and functions, we create objects that have:
- Data(information)
- Actions(what they can do)
'''

'''
Data: color,speed,model
Actions: start, stop, accelerate
'''


